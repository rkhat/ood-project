package views;

/**
 * This class holds information about how the toolbar should be displayed.
 * 
 * @author Alec Agnese, Rami El Khatib
 *
 */
public class ToolbarView {
  /**
   * True to show toolbar else hide
   */
  public boolean show = false;
  /**
   * True to show the back button else hide
   */
  public boolean showBackButton = false;
  /**
   * Title on the toolbar
   */
  public String title = "";
  /**
   * True to show the settings button else hide
   */
  public boolean showSettingsButton = false;
}
